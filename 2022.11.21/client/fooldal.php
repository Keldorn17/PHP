<h3>A diafilmekről</h3>
<div class="row">
    <div class="col-md-8"><div class="indent"><span id="bold">E</span>gy diafilm legfeljebb 200 darab 18×24 milliméteres „kockát” tartalmazó filmszalag, amely az erre alkalms diavetítő segítségével megvilágítva fehér felületre kivetíthető.</div>
    <div class="indent">Magyarországon diafilmszalagokat először a Székesfővárosi Pedagógiai Filmgyár kezdett gyártani, s 1913 és 1931 között több, színes illusztrációs anyagot tartalmazó oktatófilmet is kiadott ebben a formában. Ezek a gyakran hosszú filmtekercsek általában 80–190 darab 18×24 milliméteres kockát tartalmaztak, amelyhez a szöveges információt inzertkockák biztosították.
