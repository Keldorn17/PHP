<h1>Határátkelőhelyek</h1>
<div class="row">
    <div class="col-md-8">
        <p>A határátkelőhely az államhatár olyan pontja, ahol a határt közúton, vasúton, hajóval vagy gyalogosan át lehet lépni. Az államhatárokon kívül határátkelőhelyek vannak még a nemzetközi repülőtereken.</p>

        <p>A határátkelőhelyeken a határ átlépésekor rendszerint útlevél- és vízumellenőrzés és vámvizsgálat történik. Ezek célja, hogy megakadályozzák az illegális Bevándorlást, a drogok, fegyverek, veszélyeztetett állat- és növényfajok, illetve más illegális és veszélyes anyagok csempészetét, továbbá hogy lehetővé tegyék az árukereskedelmet terhelő vám beszedését.</p>

        <p>A schengeni egyezményhez csatlakozott országok közötti ún. „schengeni határok” átlépésekor azonban sem útlevél- és vízumellenőrzést, sem pedig vámvizsgálatot nem végeznek.</p>
